// IN NO EVENT SHALL BRISBANE SILICON, PTY LTD. BE LIABLE FOR ANY
// INCIDENTAL, PUNITIVE, OR CONSEQUENTIAL DAMAGES OF ANY KIND WHATSOEVER
// ARISING FROM THE USE OF THIS SOURCE CODE.
//
// THIS DISCLAIMER OF WARRANTY EXTENDS TO THE USER OF THIS SOURCE CODE
// AND USER'S CUSTOMERS, EMPLOYEES, AGENTS, TRANSFEREES, SUCCESSORS,
// AND ASSIGNS.
//
// THIS IS NOT A GRANT OF PATENT RIGHTS
//
// -------------------------------------------------------------------------
// DESCRIPTION : Auto-generated top wrapper.
//
// -------------------------------------------------------------------------
// SPECIFICATION :
//
// -------------------------------------------------------------------------

``timescale 1ns/1ps

import util::*;

module autogen_top_wrapper #(
    parameter reg   [(8*VERSION_CHARS)-1:0] VERSION                 = "$buildVersion",
    parameter int                           VERSION_LEN             = $buildVersionLen,

    parameter int                           CLK_FREQUENCY_MHZ       = $ClockFrequencyMhz,
    parameter int                           PUSHBUTTON0_AS_RESET    = $PushbuttonReset,
    parameter int                           EMBEDDED_LOGIC_ANALYZER = $EmbeddedLogicAnalyzer

) (
    input                               pad_clk_27Mhz,

    input   [2:1]                       pad_user_buttons_n,
    output                              pad_led_n,
    inout   [16:1]                      pad_io,
        // NOTE: Indexed to match PIN
        // numbers...
    inout   [17:0]                      pad_cpu,

    // NOTE: these ports are needed,
    // or the PSRAM IP will not
    // compile...
    output  [CS_WIDTH-1:0]              O_psram_ck,
    output  [CS_WIDTH-1:0]              O_psram_ck_n,
    inout   [CS_WIDTH-1:0]              IO_psram_rwds,
    inout   [DQ_WIDTH-1:0]              IO_psram_dq,
    output  [CS_WIDTH-1:0]              O_psram_reset_n,
    output  [CS_WIDTH-1:0]              O_psram_cs_n,

    // NOTE: these ports are needed,
    // for the GW_JTAG module to be
    // wired up transparently...
    input   wire                        tms_pad_i,
    input   wire                        tck_pad_i,
    input   wire                        tdi_pad_i,
    output  wire                        tdo_pad_o
);
localparam VERSION_CHARS    = 63;
localparam DQ_WIDTH         = 16;
localparam CS_WIDTH         = 2;


    // ----------------------------------------------
    //  Internal signals
    // ----------------------------------------------

    reg                 i_sysclk;
    reg                 i_sysclk_resetn  = 1'b0;

    reg                 i_microsecond_tick;
    reg                 i_millisecond_tick;
    reg                 i_second_tick;

    reg         [11:0]  i_millisecond_counter;

    reg         [15:0]  i_user_probe;

    reg                 i_jtag_activity;

    reg         [31:0]  i_cpu_addr;
    reg         [31:0]  i_cpu_wdata;
    reg         [3:0]   i_cpu_wstrb;
    reg         [31:0]  i_cpu_rdata;
    reg                 i_cpu_valid;
    reg                 i_cpu_ready;

    reg         [31:0]  i_ram_addr;
    reg         [31:0]  i_ram_wdata;
    reg         [3:0]   i_ram_wstrb;
    reg         [31:0]  i_ram_rdata;
    reg                 i_ram_valid;
    reg                 i_ram_ready;

    reg                 i_top_led;
    reg         [2:0]   i_ela_led;


    // ----------------------------------------------
    //  Implementation
    // ----------------------------------------------

    top #(
        .VERSION                    (VERSION),
        .CLK_FREQUENCY_MHZ          (CLK_FREQUENCY_MHZ),
        .PUSHBUTTON0_AS_RESET       (PUSHBUTTON0_AS_RESET)
    ) top_inst (

        // ---------------- pads ----------------

        .clk_27Mhz                  (pad_clk_27Mhz),

        .user_pushbutton0_n         (pad_user_buttons_n[1]),
        .user_pushbutton1_n         (pad_user_buttons_n[2]),

        .led                        (i_top_led),

        .cpu                        (pad_cpu),

        .psram_ck                   (O_psram_ck),
        .psram_ck_n                 (O_psram_ck_n),
        .psram_rwds                 (IO_psram_rwds),
        .psram_dq                   (IO_psram_dq),
        .psram_reset_n              (O_psram_reset_n),
        .psram_cs_n                 (O_psram_cs_n),


        // -------------- fabric --------------

        .sysclk                     (i_sysclk),
        .sysclk_resetn              (i_sysclk_resetn),

        .microsecond_tick           (i_microsecond_tick),
        .millisecond_tick           (i_millisecond_tick),
        .second_tick                (i_second_tick),

        .millisecond_counter        (i_millisecond_counter),


        // -------------- cpu --------------

        .cpu_addr                   (i_cpu_addr),
        .cpu_wdata                  (i_cpu_wdata),
        .cpu_wstrb                  (i_cpu_wstrb),
        .cpu_rdata                  (i_cpu_rdata),
        .cpu_valid                  (i_cpu_valid),
        .cpu_ready                  (i_cpu_ready),

            // TODO: streaming interface as well... ?

        // -------------- memory --------------

        .ram_addr                   (i_ram_addr),
        .ram_wdata                  (i_ram_wdata),
        .ram_wstrb                  (i_ram_wstrb),
        .ram_rdata                  (i_ram_rdata),
        .ram_valid                  (i_ram_valid),
        .ram_ready                  (i_ram_ready)
    );


    // ----------------------------------------------
    //  User
    // ----------------------------------------------

    user user_inst (
        .sysclk             (i_sysclk),
        .sysclk_resetn      (i_sysclk_resetn),

        .microsecond_tick   (i_microsecond_tick),
        .millisecond_tick   (i_millisecond_tick),
        .second_tick        (i_second_tick),

        .io                 (pad_io),

        .cpu_addr           (i_cpu_addr),
        .cpu_wdata          (i_cpu_wdata),
        .cpu_wstrb          (i_cpu_wstrb),
        .cpu_rdata          (i_cpu_rdata),
        .cpu_valid          (i_cpu_valid),
        .cpu_ready          (i_cpu_ready),

        .ram_addr           (i_ram_addr),
        .ram_wdata          (i_ram_wdata),
        .ram_wstrb          (i_ram_wstrb),
        .ram_rdata          (i_ram_rdata),
        .ram_valid          (i_ram_valid),
        .ram_ready          (i_ram_ready),

        .probe              (i_user_probe)
    );


    // ----------------------------------------------
    //  ELA Example
    // ----------------------------------------------

    generate
        if(EMBEDDED_LOGIC_ANALYZER) begin

            // ----------------------------------------------
            //  Internal signals (ELA-internal)
            // ----------------------------------------------

            localparam ELA_SAMPLE_WIDTH = 8;
            localparam ELA_SAMPLE_DEPTH = 64;
            localparam ELA_CHANNELS     = 6;
                // NOTE: modify probe dimensions as required...

            reg                                         i_sysclk_reset;

            reg [1:0]                                   i_buttons;
            reg [ELA_SAMPLE_WIDTH-1:0]                  i_counter;

            reg [(ELA_SAMPLE_WIDTH*ELA_CHANNELS)-1:0]   i_probe;


            always @(posedge i_sysclk) begin
                if (i_sysclk_resetn == 1'b0) begin
                    i_counter   <= 0;
                    i_buttons   <= 0;
                end else begin
                    i_counter   <= i_counter + 1'b1;
                    i_buttons   <= ~pad_user_buttons_n;
                end
            end

            assign i_probe = { 7'b0000000, i_buttons[1], i_counter, i_user_probe};
                // NOTE: modify probe regs as required...

            // ----------------------------------------------
            //  Embedded Logic Analyser
            // ----------------------------------------------

            assign i_sysclk_reset = ~i_sysclk_resetn;

            fcapz_ela_gowin #(
                .SAMPLE_W       (ELA_SAMPLE_WIDTH),
                .DEPTH          (ELA_SAMPLE_DEPTH),
                .NUM_CHANNELS   (ELA_CHANNELS),
                .EIO_EN         (0)
            ) u_ela (
                .clk            (i_sysclk),
                    // NOTE: this clock must be
                    // at least ~10x the JTAG TCK
                    // (~2 MHz for EPM11)
                .jtag_activity  (i_jtag_activity),

                .sample_clk     (i_sysclk),
                .sample_rst     (i_sysclk_reset),
                .probe_in       (i_probe),

                .eio_probe_in   (0),
                .eio_probe_out  (),
                    // NOTE: external trigger ports
                    // tie off if not used

                .tms_pad_i      (tms_pad_i),
                .tck_pad_i      (tck_pad_i),
                .tdi_pad_i      (tdi_pad_i),
                .tdo_pad_o      (tdo_pad_o)
            );

            // NOTE: We control the
            // leds in this case
            // ---------------------

            always @(posedge i_sysclk) begin
                if (i_millisecond_tick == 1'b1) begin
                    if (i_millisecond_counter == 0 || i_millisecond_counter == 500) begin
                        i_ela_led[0] <= ~i_ela_led[0];
                    end
                end

                if (i_jtag_activity == 1'b1) begin
                    i_ela_led[1] <= 1'b1;
                end
                if (|i_millisecond_counter[6:0] == 1'b0) begin
                    i_ela_led[1] <= 0;
                end

                i_ela_led[2] <= i_buttons[1];

                if (i_sysclk_resetn == 1'b0) begin
                    i_ela_led <= 0;
                end
            end

            assign pad_led_n = ~(|i_ela_led);

        end else begin

            assign tdo_pad_o = 1'b0;

            assign pad_led_n = ~i_top_led;
        end
    endgenerate

endmodule
"@

# ---- WRITE TO FILE ----
$BuildArtifactsDirectory = $ExecutionContext.SessionState.Path.GetUnresolvedProviderPathFromPSPath($BuildArtifactsDirectory)
$outputPath = Join-Path $BuildArtifactsDirectory $TopWrapperFilename

# create directory if it doesn't exist
if (-not (Test-Path $BuildArtifactsDirectory)) {
    Write-Host "  Creating artifacts directory: $BuildArtifactsDirectory"
    New-Item -ItemType Directory -Path $BuildArtifactsDirectory -Force | Out-Null
}

# write file — UTF-8 without BOM to match Linux output
[System.IO.File]::WriteAllText($outputPath, $content, [System.Text.UTF8Encoding]::new($false))

Write-Host "  Generated   : $outputPath"
