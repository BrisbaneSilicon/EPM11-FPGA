# =============================================================
# generate_top_wrapper.ps1
# Generates autogen_top_wrapper.sv with current build settings.
# Called by build_win_v2.ps1 before running gw_sh.exe
# =============================================================
#
# Stage 1B: Added 18 memory bus internal wires and wired them to top_inst,
# user_inst (user branch), and tied idle in board_demonstration_inst (demo branch).
# =============================================================

param (
    [string]$BuildArtifactsDirectory,   # where to save the file
    [string]$TopWrapperFilename,        # filename e.g. "autogen_top_wrapper.sv"
    [int]$ClockFrequencyMhz = 51,       # system clock frequency
    [int]$PushbuttonReset   = 1,        # pushbutton reset enabled
    [int]$EmbeddedLogicAnalyzer = 0     # 0 = disabled, 1 = enabled
)


# ---- VALIDATE REQUIRED ARGUMENTS ----
if (-not $BuildArtifactsDirectory) {
    Write-Host "ERROR: -BuildArtifactsDirectory is required."
    Write-Host "Usage: .\generate_top_wrapper.ps1 -BuildArtifactsDirectory <path> -TopWrapperFilename <filename>"
    exit 1
}
if (-not $TopWrapperFilename) {
    Write-Host "ERROR: -TopWrapperFilename is required."
    Write-Host "Usage: .\generate_top_wrapper.ps1 -BuildArtifactsDirectory <path> -TopWrapperFilename <filename>"
    exit 1
}


# ---- GET GIT INFO ----
$gitShortRev     = git rev-parse --short HEAD
$gitLocalChanges = git diff-index HEAD
$gitDirty        = if ($gitLocalChanges) { "-dirty" } else { "" }
$timeStr         = Get-Date -Format "yyyy-MM-dd HH:mm:ss"
$buildVersion    = "FW: ${gitShortRev}${gitDirty}|${timeStr}"
$buildVersionLen = $buildVersion.Length

Write-Host "  Git commit  : $gitShortRev$gitDirty"
Write-Host "  Build time  : $timeStr"
Write-Host "  Version str : $buildVersion"

# ---- GENERATE FILE CONTENT ----
# note: backtick in systemverilog (e.g. `timescale) must be written
# as double backtick (``) inside a PowerShell here-string
$content = @"
//
//                          !!! WARNING !!!
//
//     This file is autogenerated - modifications will not persist !
//
//

// -------------------------------------------------------------------------
